<h1>Using Planet Biru Image Editor</h1>
<h2>File
</h2>
<h3>Open File</h3>
<p>To edit image from local disk, you should to open open that file. </p>
<p>Browse and select file from your local disk. Planet Biru Image Editor will open that file without uploading it to the server.</p>
<h2>Edit</h2>
<h3>Undo</h3>
<p>Undo will back to previous change point of the editing.</p>
<h3>Redo</h3>
<p>Redo will go to next change point of the editing.</p>
<h2>Image</h2>
<h3>Brightness and Contrast</h3>
<p>Brightness and Contrast allow you to modify the brightness and the contrast of the image by adding or reducing the value using the slider. The status of the slider will be displayed on the dialog.</p>
<h3>Adjust Color</h3>
<p>Adjust Color allow you to modify the hue, saturation, and luminance of the image using the slider. The status of the slider will be displayed on the dialog.</p>
<h3>Color Balance</h3>
<p>Color Balance allow you to modify the Red, Green, and Blue component of the image. The status of the slider will be displayed on the dialog.</p>
<h3>Invert Color</h3>
<p>Invert Color will invert the color of the image.</p>
<h3>Gray Scale</h3>
<p>Gray Scale will convert the image to the gray scale image. The image will lost its color information. You can <strong>Undo</strong> to restore the color information.</p>
<h3>Opacity</h3>
<p>Opacity will change the opacity of the image. The opacity range value is 0 to 255 that meaning the image with opacity 255 is visible and image with opacity 0 is invisible. The transparency is the antonym of the opacity.</p>
<h2>Transform</h2>
<h3>Flip Vertical</h3>
<p>Flip Vertical will flip the image verticaly</p>
<h3>Flip Horizontal</h3>
<p>Flip Vertical will flip the image horizontaly</p>
<h3>Rotate Clockwise</h3>
<p>Rotate Clockwise will rotate the image clockwise</p>
<h3>Rotate Counterclockwise</h3>
<p>Rotate Clockwise will rotate the image counterclockwise</p>
